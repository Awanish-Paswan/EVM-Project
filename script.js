var TotalVote=0;
function fun()
{
    TotalVote++;
    document.getElementById("vote1").innerHTML="Bjp Total vote: " + TotalVote;

    document.getElementById("button1").addEventListener("click", () => {
        const dot= document.getElementById("dot1");

        dot.style.backgroundColor ="green";

        setTimeout(() =>{
            dot.style.backgroundColor ="red";

        }, 700);
    });

    const now = new Date();
    const timeString = now.toLocaleString(); // Current date and time in the system's locale format
    
    // Create a new paragraph element for each time entry
    const timeEntry = document.createElement('p');
    timeEntry.textContent = '+1 : ' + timeString;
    
    // Append the new time entry to the container
    document.getElementById('timeContainer1').appendChild(timeEntry);

    
    
}

